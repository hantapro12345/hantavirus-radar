export function normalizeForSignalDictionary(value: string) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^\p{L}\p{N}\s'’-]/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function scoreSignalText(text: string) {
  const clean = normalizeForSignalDictionary(text);

  const matched: {
    category: SignalCategory;
    term: string;
    weight: number;
  }[] = [];

  const scores: Record<SignalCategory, number> = {
    confirmed: 0,
    suspected: 0,
    monitored: 0,
    death: 0,
    ruled_out: 0,
    health_context: 0,
    authority: 0,
    weak_signal: 0,
    noise: 0,
  };

  for (const group of hantavirusSignalDictionary) {
    for (const term of group.terms) {
      const cleanTerm = normalizeForSignalDictionary(term);

      if (!cleanTerm) continue;

      if (clean.includes(cleanTerm)) {
        scores[group.category] += group.weight;
        matched.push({
          category: group.category,
          term,
          weight: group.weight,
        });
      }
    }
  }

  return {
    scores,
    matched,
  };
}